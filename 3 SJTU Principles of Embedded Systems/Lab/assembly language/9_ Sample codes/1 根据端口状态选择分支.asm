COMMENT 1
某控制电路，输出控制端口地址为34CH，输入状态端口地址为34DH，从输入端口读入外部状态，如果不为80H，就输出FFH关闭设备，否则输入00H开启设备。
1

MOV DX, 34DH
IN  AL, DX
CMP AL, 80H
JNZ GO-OFF

MOV DX, 34CH
MOV AL, 00H
OUT DX, AL
JMP EXIT  ; break

GO-OFF: MOV DX, 34CH
MOV AL, 0FFH
OUT DX, AL

EXIT: RET


