; Description: 计算 有符号数 Y = 6X+8	
; FFFE
; FFFC
; FFF8
DAT	SEGMENT
	DATA	DB	-2
	SUM		DB	?
DAT	ENDS

STACK	SEGMENT
	ST	DB	100	DUP(?)
STACK	ENDS

CODE	SEGMENT
		ASSUME	CS:CODE, DS:DAT, SS:STACK

MAIN	PROC	FAR
START:	MOV	AX, STACK
		MOV SS, AX
		PUSH, DS
		MOV AX, 0
		PUSH AX
		MOV AX, DAT
		MOV DS, AX
		
		MOV AL, DATA
		SHL AL, 1
		MOV BL, AL
		SHL AL, 1
		ADD AL, BL
		ADD AL, 8
		MOV SUM, AL
		RET
MAIN	ENDP
CODE	ENDS
		END START
